function number(code) {
    code = Number(code);
    switch (code) {
        case 1 :
            console.log("one");
            break;
            case 2 :
            console.log("two");
                break;
                case 3 :
                    console.log("three");
                    break;
                    case 4 :
                        console.log("four");
                        break;
                        case 5 :
                            console.log("five");
                            break;
                            default:
                                console.log("not an ordinary number");
                                

    }

    

}
number(5);